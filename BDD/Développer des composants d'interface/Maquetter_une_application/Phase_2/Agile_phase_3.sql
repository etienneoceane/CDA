Donnez une définition de l'agilité:

La méthode agile, est une méthode d'organisation de travail composée de sprints, de temps de paroles afin de définir 
le travail fait et à faire ainsi que les problèmes à résoudre dans la journée.



Pourquoi l'Extreme Programming ?

l'Extreme Programming est une méthode agile de gestion de projet informatique adaptée aux équipes réduites avec des besoins changeants.
Elle pousse à l'extrême des principes simples.
Dans les méthodes traditionnelles, les besoins sont définis, et souvent fixés, au départ du projet infomatique, ce qui accroît les coûts ultérieurs de modifications.
l' XP s´attache à rendre le projet plus flexible et ouvert au changement en introduisant des valeurs de base, des principes et des pratiques.
(revue de code, tests, conception, simplicité, compréhension, push)



Quels avantages y a-t'il à utiliser SCRUM ?

Modification, ajout ou suppression de fonctionnalité pendant le developpement.
Il permet aussi d'aboutir à un "premier jet" utilisable.
Validés au fur et a mesuere par le client, les sprints suivant permettent d'enrichir celui-ci.



Sprint et intégration continue, expliquez le fonctionnement d'un sprint

Un sprint est une période de développement qui dure de 1 à 4 semaines.



TDD :(Test Driven Development), recherchez des informations pour expliquer le principe.

Le principe est de d'abord créer les tests avant de developper le projet, ce qui augment la qualité du projet, et reduit les couts de maintenance